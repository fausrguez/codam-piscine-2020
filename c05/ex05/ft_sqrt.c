int	ft_sqrt(int nb)
{
	int n;

	n = 0;
	if (nb <= 0)
	{
		return (0);
	}
	while (n * n < nb && n <= 46340)
	{
		n++;
	}
	if (n * n == nb)
	{
		return (n);
	}
	else
	{
		return (0);
	}
}
